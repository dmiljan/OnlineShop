                {
                    SeedProductType(_context);
                    SeedAttribute(_context);
                    SeedProductTypeAttribute(_context);
                    SeedAtributeValue(_context);
                    SeedProductTypeAttributeValue(_context);

                    transaction.Commit();
                }
            }
        }

